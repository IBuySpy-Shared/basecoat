#!/usr/bin/env pwsh
[CmdletBinding()]
param(
    [Parameter(Mandatory = $true)]
    [string]$Experiment,
    [string]$PromptsFile = "",
    [string]$OutputDir = "test-results"
)

$ErrorActionPreference = "Stop"

function Get-WordCount {
    param([string]$Text)
    if (-not $Text) { return 0 }
    return (@($Text -split '\s+' | Where-Object { $_ -and $_.Trim().Length -gt 0 })).Count
}

function Score-Response {
    param(
        [string]$Response,
        [array]$Criteria,
        [int]$MaxWords
    )
    $criteria = @($Criteria)
    $hits = 0
    foreach ($c in $criteria) {
        if ($Response -match [regex]::Escape($c)) { $hits++ }
    }
    $ratio = if ($criteria.Count -gt 0) { $hits / $criteria.Count } else { 0.5 }

    $words = Get-WordCount -Text $Response
    $lengthPenalty = 0
    if ($MaxWords -gt 0 -and $words -gt $MaxWords) {
        $lengthPenalty = [Math]::Min(0.4, (($words - $MaxWords) / [Math]::Max(1, $MaxWords)))
    }

    $score = [Math]::Max(0, [Math]::Round((10 * $ratio) - (10 * $lengthPenalty), 2))
    return [PSCustomObject]@{
        score = $score
        hits = $hits
        criteriaCount = $criteria.Count
        wordCount = $words
    }
}

function Get-Rationale {
    param(
        [string]$Winner,
        $A,
        $B
    )
    if ($Winner -eq "tie") {
        return "Both variants scored similarly (A=$($A.score), B=$($B.score)); no statistically meaningful difference in criteria coverage."
    }
    if ($Winner -eq "v1") {
        return "v1 wins due to stronger criteria coverage ($($A.hits)/$($A.criteriaCount)) and better score ($($A.score) vs $($B.score))."
    }
    return "v2 wins due to stronger criteria coverage ($($B.hits)/$($B.criteriaCount)) and better score ($($B.score) vs $($A.score))."
}

$repoRoot = Resolve-Path (Join-Path $PSScriptRoot '..')
Set-Location $repoRoot

$experimentDir = Join-Path $repoRoot "tests/experiments/$Experiment"
if (-not (Test-Path $experimentDir)) {
    throw "Experiment directory not found: tests/experiments/$Experiment"
}

if (-not $PromptsFile) {
    $PromptsFile = Join-Path $experimentDir "prompts.json"
}
if (-not (Test-Path $PromptsFile)) {
    throw "Prompts file not found: $PromptsFile"
}

$v1 = Join-Path $experimentDir "v1.agent.md"
$v2 = Join-Path $experimentDir "v2.agent.md"
if (-not (Test-Path $v1)) { throw "Missing variant file: $v1" }
if (-not (Test-Path $v2)) { throw "Missing variant file: $v2" }

$data = Get-Content $PromptsFile -Raw | ConvertFrom-Json
$cases = @($data.cases)
if ($cases.Count -eq 0) {
    throw "No cases found in $PromptsFile"
}

if (-not (Test-Path $OutputDir)) {
    New-Item -ItemType Directory -Path $OutputDir -Force | Out-Null
}

$rows = @()
$wins = @{ v1 = 0; v2 = 0; tie = 0 }

foreach ($case in $cases) {
    if (-not $case.v1_response -or -not $case.v2_response) {
        throw "Case '$($case.id)' is missing v1_response or v2_response (fixture mode required in phase 1)."
    }
    $a = Score-Response -Response $case.v1_response -Criteria @($case.criteria) -MaxWords ([int]$case.max_words)
    $b = Score-Response -Response $case.v2_response -Criteria @($case.criteria) -MaxWords ([int]$case.max_words)
    $winner = if ($a.score -gt $b.score) { "v1" } elseif ($b.score -gt $a.score) { "v2" } else { "tie" }
    $wins[$winner]++
    $rows += [PSCustomObject]@{
        id = $case.id
        prompt = $case.prompt
        winner = $winner
        v1Score = $a.score
        v2Score = $b.score
        rationale = (Get-Rationale -Winner $winner -A $a -B $b)
    }
}

$overallWinner = if ($wins.v1 -gt $wins.v2) { "v1" } elseif ($wins.v2 -gt $wins.v1) { "v2" } else { "tie" }
$jsonPath = Join-Path $OutputDir "$Experiment-ab-report.json"
$mdPath = Join-Path $OutputDir "$Experiment-ab-report.md"

$report = [PSCustomObject]@{
    experiment = $Experiment
    generatedAt = (Get-Date).ToUniversalTime().ToString("o")
    cases = $rows
    summary = [PSCustomObject]@{
        v1Wins = $wins.v1
        v2Wins = $wins.v2
        ties = $wins.tie
        overallWinner = $overallWinner
    }
}
$report | ConvertTo-Json -Depth 8 | Out-File -FilePath $jsonPath -Encoding utf8

$lines = @()
$lines += "# A/B Experiment Report — $Experiment"
$lines += ""
$lines += "- v1 wins: **$($wins.v1)**"
$lines += "- v2 wins: **$($wins.v2)**"
$lines += "- ties: **$($wins.tie)**"
$lines += "- overall winner: **$overallWinner**"
$lines += ""
$lines += "| Case | Winner | v1 | v2 | Rationale |"
$lines += "|---|---|---:|---:|---|"
foreach ($r in $rows) {
    $lines += "| $($r.id) | $($r.winner) | $($r.v1Score) | $($r.v2Score) | $($r.rationale) |"
}
$lines += ""
$lines += "_Generated by scripts/run-experiment.ps1_"
$lines -join "`n" | Out-File -FilePath $mdPath -Encoding utf8

Write-Output ($lines -join "`n")
